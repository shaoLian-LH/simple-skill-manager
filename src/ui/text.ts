function resolveMessage(locale: UiLocale, key: string): string | null {
  const segments = key.split('.');
  let current: UiTextValue | undefined = MESSAGES[locale];

  for (const segment of segments) {
    if (!current || typeof current === 'string') {
      return null;
    }
    current = current[segment];
  }

  return typeof current === 'string' ? current : null;
}

function interpolate(template: string, params?: Record<string, string | number>): string {
  if (!params) {
    return template;
  }

  return template.replace(/\{\{(\w+)\}\}/g, (_match, key: string) => {
    const value = params[key];
    return value === undefined ? '' : String(value);
  });
}

export function parseUiLocale(value: unknown): UiLocale | null {
  const candidate = Array.isArray(value) ? value[0] : value;
  return candidate === 'zh-CN' || candidate === 'en-US' ? candidate : null;
}

export function normalizeUiLocale(value: unknown, fallback: UiLocale = DEFAULT_UI_LOCALE): UiLocale {
  return parseUiLocale(value) ?? fallback;
}

export function translateUiText(
  locale: UiLocale,
  key: string,
  params?: Record<string, string | number>,
): string {
  const message = resolveMessage(locale, key) ?? resolveMessage(DEFAULT_UI_LOCALE, key) ?? key;
  return interpolate(message, params);
}

export function formatUiDateTime(locale: UiLocale, value?: string | null): string {
  if (!value) {
    return translateUiText(locale, 'time.unknown');
  }

  const date = new Date(value);
  if (Number.isNaN(date.getTime())) {
    return String(value);
  }

  return new Intl.DateTimeFormat(locale, {
    dateStyle: 'medium',
    timeStyle: 'short',
  }).format(date);
}

export function formatUiRelativeTime(locale: UiLocale, value?: string, now = Date.now()): string {
  if (!value) {
    return translateUiText(locale, 'time.unknown');
  }

  const parsed = new Date(value);
  if (Number.isNaN(parsed.getTime())) {
    return String(value);
  }

  const diffMs = now - parsed.getTime();
  const future = diffMs < 0;
  const absMs = Math.abs(diffMs);
  const minute = 60 * 1000;
  const hour = 60 * minute;
  const day = 24 * hour;

  if (absMs < minute) {
    return translateUiText(locale, future ? 'time.underMinuteFuture' : 'time.justNow');
  }

  if (absMs < hour) {
    const minutes = Math.round(absMs / minute);
    return translateUiText(locale, future ? 'time.minuteIn' : 'time.minuteAgo', { count: minutes });
  }

  if (absMs < day) {
    const hours = Math.round(absMs / hour);
    return translateUiText(locale, future ? 'time.hourIn' : 'time.hourAgo', { count: hours });
  }

  const days = Math.round(absMs / day);
  if (days === 1) {
    return translateUiText(locale, future ? 'time.tomorrow' : 'time.yesterday');
  }

  return translateUiText(locale, future ? 'time.dayIn' : 'time.dayAgo', { count: days });
}

